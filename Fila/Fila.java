public class Fila {
    private Node frente;
    private Node tras;



    public Fila(){}

    public void setFrente(Node frente) {
        this.frente = frente;
    }

    public void setTras(Node tras) {
        this.tras = tras;
    }

    public Node getFrente() {
        return frente;
    }

    public Node getTras() {
        return tras;
    }
}


